void Map::LoadMap(int arr[20][50]){
    for (int row = 0; row < 20; row++){
        for (int col = 0; col < 50; col ++){
            map[row][col] = arr[row][col];
        }
    }
}

Map::Map(){
    dirt = TextureManager::Load_texture("image/grass.png");
    grass = TextureManager::Load_texture("image/grass.png");
    water = TextureManager::Load_texture("image/water.png");
    flower = TextureManager::Load_texture("image/bush.png");

    LoadMap(lv1);

    src.x = src.y = 0;
    src.w = dest.w = 32;
    src.h = dest.h = 32;
    dest.x = dest.y = 0;


}


void Map::DrawMap(){
    int type = 0;
    for (int row = 0; row < 20; row++){
        for (int col = 0; col < 50; col ++){
            type = map[row][col];
            dest.x = col*32 - Game::camera.x - 400;
            dest.y = row*32;
            switch (type){
                case 0:


                    break;
                case 1:
                    src.x = 32;
                    src.y = 32;
                    TextureManager::Draw(grass, src, dest);
                    break;
                case 2:
                    src.x = 32;
                    src.y = 0;

                    TextureManager::Draw(dirt, src, dest);
                    break;

                case 3:
                    src.x = 0;
                    src.y = 0;
                    TextureManager::Draw(flower, src, dest);
                    break;
                default:
                    break;
            }
        }
    }
}

Map::~Map(){
    SDL_DestroyTexture(dirt);
    SDL_DestroyTexture(water);
    SDL_DestroyTexture(grass);
}
